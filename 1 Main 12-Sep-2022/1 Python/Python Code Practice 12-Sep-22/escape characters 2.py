print('\'Faizul\'\n Ahamed \n') #Single Quote & New Line

print('\"Faizul\"') #Double Quote
print('"Faizul"') #Double Quote used in single quote No error
#print(""Faizul"") #Double Quote used in double quote so it's an error
print("\"Faizul\" \n") #Double Quote used in double quote using escape character so no error.

print('\""Faizul\""') #Double Quote 2 times
print("\"""Faizul\""" \n") #Double Quote 3 times output is only 1 double quotes

print('\\Faizul \\Ahamed') #	Backslash
print('\\\Faizul \\\Ahamed \n') # Backslash 2 times

print('Faizul\r Ahamed') # Carriage Return / \r sent the print head back to the start of the line
print('Faizul\r Ahamed\r Shayan\r') # If last \r then if no any string it won't go to next line
print('Faizul\r Ahamed\r Shayan\n') # If last \n then if no any string it will go to next line

print('Faizul age is\t 39')  #Tab
faiz = ("Faizul age is \t 39 \n")
print (faiz)

backspace = ("Faizul  \bAhamed") #Backspace
print (backspace)
